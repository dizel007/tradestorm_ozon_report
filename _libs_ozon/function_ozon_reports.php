function get_article_by_sku_fbs($ozon_sebest, $sku) {
    foreach ($ozon_sebest as $item_cat) {
        if (($sku == $item_cat['sku']) OR (($sku == $item_cat['skuFBO']))) {
            $article = $item_cat['mp_article'];
            break;
        } else {$article  = 'SKU = '.$sku;}

    }
    
    return $article;
    }

