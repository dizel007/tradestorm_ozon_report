function change_SKU_fbo_fbs($ozon_sebest, $sku){
    /// Функия подменяет СКУ ФБО на СКУ ФБС - 
        foreach ($ozon_sebest as $item_cat) {
            if ($sku == $item_cat['skuFBO']) {
                $new_sku  = $item_cat['sku'];
                break;
            } else {$new_sku  = $sku;}
    
        }
        
        return $new_sku;
        }

